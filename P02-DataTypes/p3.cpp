#include<iostream> 
using namespace std; 
int main() { 
    float x; 
    cout << "Enter a float num: ";
    cin >> x; 
    x++; 
    cout << x << endl;  
    x++; 
    cout << x << endl;  
    x+=5; 
    cout << x << endl;  
    x-=5; 
    cout << x << endl;  
    x*=5; 
    cout << x << endl;  
    x/=5; 
    cout << x << endl;  
    x--;
    cout << x << endl;  
    x--;
    cout << x << endl;  
}